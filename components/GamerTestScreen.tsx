const getQuestionsForLevel = (level: number): Question[] => {
    const rawData = RAW_QUESTIONS_DATA[level];
    if (!rawData || rawData.length === 0) {
        return []; 
    }
    const shuffledQuestionsPool = shuffleArray(rawData);
    return shuffledQuestionsPool.map((q, i) => {
        const originalCorrectText = q.opts[q.ans];
        const shuffledOpts = shuffleArray(q.opts);
        const newAnsIndex = shuffledOpts.indexOf(originalCorrectText);
        return {
            id: (level * 1000) + i,
            question: q.question || q.q,
            options: shuffledOpts,
            correct: newAnsIndex
        };
    });
};

